public class HTList {
    
    private String ID;
    private short IDRadio;
    private String SAP;
    private String SN;

    public String getID() {
        return ID;
    }

    public void setID(String ID) {
        this.ID = ID;
    }

    public short getIDRadio() {
        return IDRadio;
    }

    public void setIDRadio(short IDRadio) {
        this.IDRadio = IDRadio;
    }

    public String getSAP() {
        return SAP;
    }

    public void setSAP(String SAP) {
        this.SAP = SAP;
    }

    public String getSN() {
        return SN;
    }

    public void setSN(String SN) {
        this.SN = SN;
    }
    
}
